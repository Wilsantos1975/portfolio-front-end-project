function manyCocktails(cocktail, input) {
  let ul = document.createElement("ul");

  cocktail.drinks.map((cocktail) => {
    let li = document.createElement("li");
    li.innerHTML = `<a href="#">${cocktail.strDrink}</a>`;
    ul.append(li);
  });
  main.append(ul);
}

